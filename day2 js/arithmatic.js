var first = 10;
var second = 3;

var add = first + second;
console.log(add);

var sub = first - second;
console.log(sub);

var mul = first * second;
console.log(mul);

var div = first / second;
console.log(div);

var mod = first % second;
console.log(mod);

//pre increment
var inc = ++first;
var dec = --second
console.log(inc, dec);

//post increment & decrement
var inc = first++;
var dec = second--;
console.log(inc, dec);

